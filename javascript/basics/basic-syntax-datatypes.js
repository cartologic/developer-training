// Quick overview on JavaScript syntax and basics

// Create a variable:
var myVar = "any var"
myVar = 10            // changed variable to 10

// Create a constant
const myNumber = 10
myNumber = 20 // will throw an error can't change such a const

// primitive data types
// String:
const firstName = 'John'
const lastName = 'Adams'
const fullName = 'John' + ' ' + 'Adams'

// Numbers:
const age = 10
const doubledAge = age * 2
const weight = 65.33
const degreeF = 32
const degreeC = (degreeF - 32) * (5 / 9)
